/**
 * GRIM 팁 한 묶음을 무작위로 고른다 (던전 상점용).
 * @param {number} [floorNum=1] GRIM 이 있는 층 — 구역 3·4(21층~)면 ZONE34 풀, 아니면 ZONE12 풀을 섞는다.
 */
export function randomGrimTip(floorNum = 1) {
  const zonePool = zoneOf(floorNum) >= 3 ? GRIM_TIPS_ZONE34 : GRIM_TIPS_ZONE12;
  const pool = [...GRIM_TIPS_COMMON, ...zonePool];
  return pool[Math.floor(Math.random() * pool.length)];
}

/** HUB 해금상점용 팁 추첨 — 공용 + 구역 1·2 팁에 HUB 전용 회상이 섞인다. */
export function randomGrimHubTip() {
  const pool = [...GRIM_TIPS_COMMON, ...GRIM_TIPS_ZONE12, ...GRIM_TIPS_HUB];
  return pool[Math.floor(Math.random() * pool.length)];
}
